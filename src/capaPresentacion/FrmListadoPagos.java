    @Override
    public final void loadTable() {
        try {
            for (int i = this.defaultTable.getRowCount() - 1; i >= 0; i--) {
                this.defaultTable.removeRow(i);
            }

            Pagos clsPagos = new Pagos();
            ArrayList arrayList = clsPagos.getAll();

            for (int i = 0; i < arrayList.size(); i++) {
                EPagos pago = (EPagos) arrayList.get(i);
                this.defaultTable.addRow(new Object[]{});
                this.defaultTable.setValueAt(pago.getId(), i, 0);
                this.defaultTable.setValueAt(pago.getTotal(), i, 1);
                this.defaultTable.setValueAt(pago.getNumCuotas(), i, 2);
                this.defaultTable.setValueAt(pago.getEstado(), i, 3);
                this.defaultTable.setValueAt(pago.getAlumno(), i, 4);
                this.defaultTable.setValueAt(pago.getGrupo_horario(), i, 5);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        }
    }

