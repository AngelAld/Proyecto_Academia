     private void loadTable() {
        try {
            for (int i = this.defaultTable.getRowCount() - 1; i >= 0; i--) {
                this.defaultTable.removeRow(i);
            }

            Deportes clsDeportes = new Deportes();
            ArrayList arrayList = clsDeportes.getAll();

            for (int i = 0; i < arrayList.size(); i++) {
                EDeportes dep = (EDeportes) arrayList.get(i);
                this.defaultTable.addRow(new Object[]{});
                this.defaultTable.setValueAt(dep.getId(), i, 0);
                this.defaultTable.setValueAt(dep.getNombre(), i, 1);
                this.defaultTable.setValueAt(dep.getDescripcion(), i, 2);
                this.defaultTable.setValueAt(dep.getEstado(), i, 3);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.ERROR_MESSAGE);

        }
    }
